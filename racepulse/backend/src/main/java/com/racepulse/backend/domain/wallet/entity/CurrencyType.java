package com.racepulse.backend.domain.wallet.entity;

// =============================================================================
// CurrencyType.java - 지갑에서 다루는 재화 종류를 모아 둔 Enum
// =============================================================================
// Enum은 정해진 값만 사용할 수 있게 해 주는 Java 문법입니다.
// 문자열(String)로 "EVENT"를 직접 쓰면 오타가 나도 컴파일 단계에서 잡기 어렵지만,
// Enum을 쓰면 잘못된 값은 코드 작성 단계에서 바로 확인할 수 있습니다.
// =============================================================================

public enum CurrencyType {

    // EVENT = 출석, 광고, 퀴즈로 받는 이벤트 편자입니다. 6개월 만료가 있으므로 먼저 소비합니다.
    EVENT,

    // PAID = 인앱 결제로 받는 구매 편자입니다. 만료가 없으므로 이벤트 편자를 다 쓴 뒤 소비합니다.
    PAID,

    // HAY = 다마고치 전용 건초입니다. 편자와 서로 바꾸지 못하는 별도 재화입니다.
    HAY
}
