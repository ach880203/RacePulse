// -----------------------------------------------------------------------------
// 광고 시청 시간 타입 — 15초 / 30초 / 60초만 허용
// -----------------------------------------------------------------------------
// '15 | 30 | 60' = 세 숫자 중 하나만 올 수 있다는 약속입니다.
// 다른 숫자(예: 45)를 넣으면 TypeScript가 에러를 냅니다.
export type AdDuration = 15 | 30 | 60

// -----------------------------------------------------------------------------
// WalletResponse — 지갑 조회 API 응답 형태
// -----------------------------------------------------------------------------
export interface WalletResponse {
  totalHorseshoe: number   // 보유 편자(🔩) 수량
  totalHay: number         // 보유 건초(🥇) 수량 — 광고 시청 시 지급
  adWatchedToday: number   // 오늘 광고 시청 횟수
  adLimitToday: number     // 오늘 광고 시청 한도
}

// -----------------------------------------------------------------------------
// SpendResponse — 편자 소비 API 응답 형태
// -----------------------------------------------------------------------------
export interface SpendResponse {
  success: boolean           // 소비 성공 여부
  remainingHorseshoe: number // 소비 후 남은 편자 수량
  unlockedContentType: ContentType // 잠금 해제된 콘텐츠 종류
}

// -----------------------------------------------------------------------------
// Transaction — 편자 거래 내역 한 건
// -----------------------------------------------------------------------------
export interface Transaction {
  id: number
  type: 'EARN' | 'SPEND'          // 획득인지 소비인지
  amount: number                   // 편자 수량 (획득이면 +, 소비면 -)
  reason: string                   // 사유 (예: "광고 시청", "TOP_1 잠금 해제")
  createdAt: string                // 거래 시각 (ISO 8601 문자열)
}

// -----------------------------------------------------------------------------
// PageResponse — 페이지 단위 목록 응답 (거래 내역 페이지네이션용)
// -----------------------------------------------------------------------------
// 제네릭(Generic) T = 어떤 타입이든 담을 수 있는 "형틀"입니다.
// PageResponse<Transaction>으로 쓰면 items: Transaction[] 이 됩니다.
export interface PageResponse<T> {
  items: T[]        // 현재 페이지의 항목들
  totalCount: number // 전체 항목 수
  page: number       // 현재 페이지 번호 (0부터 시작)
  pageSize: number   // 페이지당 항목 수
  hasNext: boolean   // 다음 페이지 존재 여부
}
