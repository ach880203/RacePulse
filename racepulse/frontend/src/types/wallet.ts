// =============================================================================
// wallet.ts — 편자(Horseshoe) 지갑 관련 타입 정의
// =============================================================================
// 이 파일은 TypeScript "타입"만 정의합니다.
// 타입이란? 변수에 어떤 값이 들어갈 수 있는지 미리 약속해 두는 것입니다.
// 예: ContentType이 'TOP_1'이어야 한다고 정해두면, 오타를 쳤을 때 에러가 납니다.
// =============================================================================

// -----------------------------------------------------------------------------
// ContentType — 잠금 해제가 필요한 콘텐츠 종류
// -----------------------------------------------------------------------------
// '|' = OR 연산자. "이 중 하나여야 한다"는 뜻입니다.
export type ContentType =
  | 'TOP_1'           // Top-1 확률 (가장 비싼 프리미엄 정보)
  | 'ENSEMBLE'        // 앙상블 전체 결과 (모든 모델 결합 정보)
  | 'AI_PRE'          // AI 사전 해설 (경주 전 예측 설명)
  | 'COUNTERFACTUAL'  // 가상 시나리오 (경주 변수를 바꾸면 어떻게 됐을까?)
  | 'TOP_3'           // Top-3 확률 (상위 3위 예측)
  | 'AI_POST'         // AI 결과 해설 (경주 후 분석 설명)
  | 'STAT'            // 말 개별 통계 항목 (가장 저렴)
  | 'CHANGE_DETAIL'   // 변경사항 상세 (출전 정보 변경 내용)

// -----------------------------------------------------------------------------
// CONTENT_COST — 콘텐츠별 편자 비용 상수
// -----------------------------------------------------------------------------
// Record<키타입, 값타입> = 키-값 쌍으로 이루어진 객체 타입입니다.
// 프론트엔드에서도 비용을 알아야 "편자 소비하기" 버튼 텍스트를 렌더링할 수 있습니다.
// 서버에서 매번 조회하면 요청이 늘어나므로, 이렇게 상수로 두는 것이 효율적입니다.
export const CONTENT_COST: Record<ContentType, number> = {
  TOP_1: 35,          // 35편자
  ENSEMBLE: 50,       // 50편자 (가장 비쌈)
  AI_PRE: 25,         // 25편자
  COUNTERFACTUAL: 18, // 18편자
  TOP_3: 3,           // 3편자
  AI_POST: 3,         // 3편자
  STAT: 1,            // 1편자 (가장 저렴)
  CHANGE_DETAIL: 1,   // 1편자
}

// -----------------------------------------------------------------------------
